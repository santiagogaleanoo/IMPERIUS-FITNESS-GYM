// Este archivo maneja el almacenamiento local de usuarios
// VERSI√ìN CORREGIDA - Flujo de verificaci√≥n estudiantil

export interface RegisteredUser {
  id: string
  name: string
  lastName: string
  documentType: string
  documentNumber: string
  email: string
  password: string
  createdAt: string
  esEstudiante: boolean
  verificacionEstudiantePendiente: boolean
  fechaVerificacion?: string
  documentosVerificacion?: {
    tipoVerificacion: "carnet" | "portal-edu" | "boletin"
    archivos: string[]
    fechaEnvio: string
  }
  preguntaSeguridad?: string
  respuestaSeguridad?: string
}

const STORAGE_KEY = "imperius_users_database"

export class UserStorage {
  // Obtener todos los usuarios registrados
  static getUsers(): RegisteredUser[] {
    if (typeof window === "undefined") return []
    const data = localStorage.getItem(STORAGE_KEY)
    return data ? JSON.parse(data) : []
  }

  static getAllUsers(): RegisteredUser[] {
    return this.getUsers()
  }

  // Guardar usuarios
  static saveUsers(users: RegisteredUser[]): void {
    if (typeof window === "undefined") return
    localStorage.setItem(STORAGE_KEY, JSON.stringify(users))
  }

  // ‚úÖ M√âTODO MEJORADO - Marcar verificaci√≥n pendiente y guardar documentos
  static marcarVerificacionPendiente(
    userId: string, 
    tipoVerificacion: "carnet" | "portal-edu" | "boletin",
    archivos: string[] = []
  ): void {
    const users = this.getUsers();
    const userIndex = users.findIndex((u) => u.id === userId);

    if (userIndex !== -1) {
      users[userIndex].verificacionEstudiantePendiente = true;
      users[userIndex].documentosVerificacion = {
        tipoVerificacion,
        archivos,
        fechaEnvio: new Date().toISOString()
      };
      this.saveUsers(users);
      console.log(`‚úÖ Verificaci√≥n marcada como PENDIENTE para usuario: ${userId}`);
      console.log(`üìÑ Tipo de verificaci√≥n: ${tipoVerificacion}`);
      console.log(`üìé Archivos: ${archivos.length}`);
    } else {
      console.log(`‚ö†Ô∏è Usuario no encontrado: ${userId}`);
    }
  }

  // ‚úÖ M√âTODO MEJORADO - Aprobar verificaci√≥n de estudiante
  static aprobarVerificacionEstudiante(userId: string): void {
    const users = this.getUsers();
    const userIndex = users.findIndex((u) => u.id === userId);

    if (userIndex === -1) {
      throw new Error("Usuario no encontrado");
    }

    users[userIndex].esEstudiante = true;
    users[userIndex].verificacionEstudiantePendiente = false;
    users[userIndex].fechaVerificacion = new Date().toISOString();

    this.saveUsers(users);
    
    // Actualizar tambi√©n el usuario en sesi√≥n si est√° activo
    const currentUser = localStorage.getItem("imperius_current_user");
    if (currentUser) {
      const userData = JSON.parse(currentUser);
      if (userData.id === userId) {
        userData.esEstudiante = true;
        userData.verificacionEstudiantePendiente = false;
        userData.fechaVerificacion = users[userIndex].fechaVerificacion;
        localStorage.setItem("imperius_current_user", JSON.stringify(userData));
      }
    }

    console.log(`‚úÖ USUARIO VERIFICADO COMO ESTUDIANTE: ${userId}`);
    console.log(`üéì Ahora tiene acceso a descuentos estudiantiles`);
  }

  // ‚úÖ M√âTODO MEJORADO - Rechazar verificaci√≥n de estudiante
  static rechazarVerificacionEstudiante(userId: string): void {
    const users = this.getUsers();
    const userIndex = users.findIndex((u) => u.id === userId);

    if (userIndex === -1) {
      throw new Error("Usuario no encontrado");
    }

    users[userIndex].verificacionEstudiantePendiente = false;
    users[userIndex].documentosVerificacion = undefined;
    
    this.saveUsers(users);

    // Actualizar tambi√©n el usuario en sesi√≥n si est√° activo
    const currentUser = localStorage.getItem("imperius_current_user");
    if (currentUser) {
      const userData = JSON.parse(currentUser);
      if (userData.id === userId) {
        userData.verificacionEstudiantePendiente = false;
        localStorage.setItem("imperius_current_user", JSON.stringify(userData));
      }
    }

    console.log(`‚ùå Verificaci√≥n RECHAZADA para usuario: ${userId}`);
  }

  // Obtener usuario por email
  static getUserByEmail(email: string): RegisteredUser | null {
    const users = this.getUsers()
    return users.find((u) => u.email.toLowerCase() === email.toLowerCase()) || null
  }

  // Obtener usuario por ID
  static getUserById(userId: string): RegisteredUser | null {
    const users = this.getUsers()
    return users.find((u) => u.id === userId) || null
  }

  // Verificar si es estudiante
  static esUsuarioEstudiante(userId: string): boolean {
    const users = this.getUsers()
    const user = users.find((u) => u.id === userId)
    return user?.esEstudiante || false
  }

  // Verificar si tiene verificaci√≥n pendiente
  static tieneVerificacionPendiente(userId: string): boolean {
    const users = this.getUsers()
    const user = users.find((u) => u.id === userId)
    return user?.verificacionEstudiantePendiente || false
  }

  // Registrar nuevo usuario
  static registerUser(
    name: string,
    lastName: string,
    documentType: string,
    documentNumber: string,
    email: string,
    password: string,
    preguntaSeguridad?: string,
    respuestaSeguridad?: string,
  ): RegisteredUser {
    const users = this.getUsers()

    if (users.some((u) => u.email.toLowerCase() === email.toLowerCase())) {
      throw new Error("Este email ya est√° registrado")
    }

    if (users.some((u) => u.documentNumber === documentNumber)) {
      throw new Error("Este n√∫mero de documento ya est√° registrado")
    }

    const newUser: RegisteredUser = {
      id: `user_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
      name,
      lastName,
      documentType,
      documentNumber,
      email,
      password,
      createdAt: new Date().toISOString(),
      esEstudiante: false,
      verificacionEstudiantePendiente: false,
      preguntaSeguridad,
      respuestaSeguridad,
    }

    users.push(newUser)
    this.saveUsers(users)

    console.log(`‚úÖ Usuario registrado exitosamente: ${email}`)
    console.log(`Total de usuarios en la base de datos: ${users.length}`)

    return newUser
  }

  // Validar credenciales de login
  static validateLogin(email: string, password: string): RegisteredUser | null {
    const users = this.getUsers()
    const user = users.find((u) => u.email.toLowerCase() === email.toLowerCase() && u.password === password)

    if (user) {
      console.log(`‚úÖ Login exitoso para: ${email}`)
      console.log(`üéì Estado estudiante: ${user.esEstudiante}`)
      console.log(`‚è≥ Verificaci√≥n pendiente: ${user.verificacionEstudiantePendiente}`)
    } else {
      console.log(`‚ùå Login fallido para: ${email}`)
    }

    return user || null
  }

  // Cambiar contrase√±a
  static cambiarContrasena(email: string, nuevaContrasena: string): boolean {
    const users = this.getUsers()
    const userIndex = users.findIndex((u) => u.email.toLowerCase() === email.toLowerCase())

    if (userIndex === -1) return false

    users[userIndex].password = nuevaContrasena
    this.saveUsers(users)

    console.log(`‚úÖ Contrase√±a actualizada para: ${email}`)
    return true
  }

  // Sincronizar contrase√±a desde PHP
  static actualizarContrasenaDesdePHP(email: string, nuevaContrasena: string): boolean {
    const users = this.getUsers()
    const userIndex = users.findIndex((u) => u.email.toLowerCase() === email.toLowerCase())

    if (userIndex === -1) {
      console.log(`‚ö†Ô∏è Usuario ${email} no encontrado en localStorage, pero se actualiz√≥ en PHP`);
      
      // Crear usuario b√°sico si no existe
      const nuevoUsuario: RegisteredUser = {
        id: `user_php_${Date.now()}`,
        name: "Usuario",
        lastName: "Recuperaci√≥n",
        documentType: "CC",
        documentNumber: "000000",
        email: email,
        password: nuevaContrasena,
        createdAt: new Date().toISOString(),
        esEstudiante: false,
        verificacionEstudiantePendiente: false,
      }
      
      users.push(nuevoUsuario);
      this.saveUsers(users);
      console.log(`‚úÖ Usuario creado en localStorage: ${email}`);
      return true;
    }

    users[userIndex].password = nuevaContrasena;
    this.saveUsers(users);

    console.log(`‚úÖ Contrase√±a sincronizada en localStorage para: ${email}`);
    return true;
  }

  // ‚úÖ M√âTODO NUEVO - Enviar verificaci√≥n de estudiante (COMPLETO)
  static enviarVerificacionEstudiante(
    userId: string,
    tipoVerificacion: "carnet" | "portal-edu" | "boletin",
    archivos: string[],
  ): void {
    console.log("========================================")
    console.log("üì§ ENVIANDO SOLICITUD DE VERIFICACI√ìN")
    console.log("========================================")
    console.log("Usuario ID:", userId)
    console.log("Tipo de verificaci√≥n:", tipoVerificacion)
    console.log("Archivos:", archivos)
    
    // ‚úÖ MARCAR COMO PENDIENTE INMEDIATAMENTE
    this.marcarVerificacionPendiente(userId, tipoVerificacion, archivos);
    
    console.log("‚úÖ Estado actualizado: VERIFICACI√ìN PENDIENTE")
    console.log("========================================")
  }

  // ‚úÖ M√âTODO MEJORADO: Limpiar base de datos COMPLETAMENTE
  static clearDatabase(): void {
    if (typeof window === "undefined") return
    
    // Limpiar TODOS los datos
    localStorage.removeItem(STORAGE_KEY)
    localStorage.removeItem("imperius_current_user")
    localStorage.removeItem("imperius_resenas")
    
    console.log("========================================")
    console.log("üóëÔ∏è  BASE DE DATOS COMPLETAMENTE REINICIADA")
    console.log("========================================")
    console.log("‚úÖ Todos los usuarios eliminados")
    console.log("‚úÖ Sesiones activas eliminadas")
    console.log("‚úÖ Verificaciones de estudiantes reseteadas")
    console.log("‚úÖ Rese√±as eliminadas")
    console.log("========================================")
    console.log("üéØ Estado actual: Base de datos VAC√çA")
    console.log("üéØ Listo para empezar pruebas desde CERO")
    console.log("========================================")
  }

  // ‚úÖ M√âTODO MEJORADO: Resetear solo verificaciones (mantener usuarios)
  static resetearSoloVerificaciones(): void {
    const users = this.getUsers();
    
    const usersActualizados = users.map(user => ({
      ...user,
      esEstudiante: false,
      verificacionEstudiantePendiente: false,
      fechaVerificacion: undefined,
      documentosVerificacion: undefined
    }));

    this.saveUsers(usersActualizados);
    
    // Actualizar usuario en sesi√≥n si existe
    const currentUser = localStorage.getItem("imperius_current_user");
    if (currentUser) {
      const userData = JSON.parse(currentUser);
      userData.esEstudiante = false;
      userData.verificacionEstudiantePendiente = false;
      localStorage.setItem("imperius_current_user", JSON.stringify(userData));
    }
    
    console.log("========================================")
    console.log("üîÑ VERIFICACIONES DE ESTUDIANTES RESETEADAS")
    console.log("========================================")
    console.log(`‚úÖ ${users.length} usuarios actualizados`)
    console.log("‚úÖ Todos marcados como NO estudiantes")
    console.log("‚úÖ Verificaciones pendientes eliminadas")
    console.log("========================================")
  }

  // Exportar datos
  static exportData(): string {
    return JSON.stringify(this.getUsers(), null, 2)
  }

  // Verificar si email existe
  static emailExists(email: string): boolean {
    const users = this.getUsers()
    return users.some((u) => u.email.toLowerCase() === email.toLowerCase())
  }

  // Recuperar contrase√±a
  static recuperarContrasena(email: string, respuestaSeguridad: string): string | null {
    const users = this.getUsers()
    const user = users.find(
      (u) =>
        u.email.toLowerCase() === email.toLowerCase() &&
        u.respuestaSeguridad?.toLowerCase() === respuestaSeguridad.toLowerCase(),
    )

    if (user) {
      console.log(`‚úÖ Contrase√±a recuperada para: ${email}`)
      return user.password
    }

    console.log(`‚ùå Recuperaci√≥n fallida para: ${email}`)
    return null
  }

  // ‚úÖ M√âTODO MEJORADO: Obtener informaci√≥n completa del usuario
  static getUsuarioCompleto(userId: string): RegisteredUser | null {
    const users = this.getUsers()
    const user = users.find((u) => u.id === userId) || null
    
    if (user) {
      console.log(`üë§ Informaci√≥n usuario ${userId}:`);
      console.log(`   - Estudiante: ${user.esEstudiante}`);
      console.log(`   - Verificaci√≥n pendiente: ${user.verificacionEstudiantePendiente}`);
      console.log(`   - Fecha verificaci√≥n: ${user.fechaVerificacion || 'N/A'}`);
    }
    
    return user;
  }

  // ‚úÖ M√âTODO NUEVO: Ver estad√≠sticas de la base de datos
  static obtenerEstadisticas() {
    const users = this.getUsers();
    return {
      totalUsuarios: users.length,
      estudiantes: users.filter(u => u.esEstudiante).length,
      verificacionesPendientes: users.filter(u => u.verificacionEstudiantePendiente).length,
      usuariosRegulares: users.filter(u => !u.esEstudiante && !u.verificacionEstudiantePendiente).length
    };
  }

  // ‚úÖ M√âTODO NUEVO: Forzar actualizaci√≥n del estado del usuario
  static forceRefreshUserState(userId: string): void {
    const user = this.getUsuarioCompleto(userId);
    if (user) {
      const currentUser = localStorage.getItem("imperius_current_user");
      if (currentUser) {
        const userData = JSON.parse(currentUser);
        userData.esEstudiante = user.esEstudiante;
        userData.verificacionEstudiantePendiente = user.verificacionEstudiantePendiente;
        localStorage.setItem("imperius_current_user", JSON.stringify(userData));
        console.log("üîÑ Estado del usuario actualizado forzadamente");
      }
    }
  }
}

export default UserStorage;